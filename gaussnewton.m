function [] = gaussnewton(x0)

for i = 1:1:10
    c1 = x0(1);
    c2 = x0(2);

    e = 2.7;
    r = [
        c1*(e^c2)-8;
        c1*2*(e^(c2*2) - 12.3);
        c1*3*(e^(c2*3) - 15.5);
        c1*4*(e^(c2*4) - 16.8);
        c1*5*(e^(c2*5) - 17.1);
        c1*6*(e^(c2*6) - 15.8);
        c1*7*(e^(c2*7) - 15.2);
        c1*8*(e^(c2*8) - 14);
        ];

    A = [c1*e^c2 e^c2; 
        4*c1*e^(c2*2) e^(c2*2);
        9*c1*e^(c2*3) e^(c2*3); 
        16*c1*e^(c2*4) e^(c2*4);
        25*c1*e^(c2*5) e^(c2*5);
        36*c1*e^(c2*6) e^(c2*6);
        49*c1*e^(c2*7) e^(c2*7);
        64*c1*e^(c2*8) e^(c2*8);];
    AtA = A'*A;
    nAtR = -A'*r;
    v = AtA\nAtR;
    x0 = x0 + v
end