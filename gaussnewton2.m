function [] = gaussnewton2(x0)

for i = 1:1:10
    c1 = x0(1);
    c2 = x0(2);

    r = [
        c1*(0.9120^c2)- 13.7;
        c1*(0.9860^c2)- 15.9;
        c1*(1.0600^c2)- 18.5;
        c1*(1.1300^c2)-21.3;
        c1*(1.19^c2)-23.5;
        c1*(1.26^c2)- 27.2;
        c1*(1.3200^c2)-32.7;
        c1*(1.3800^c2)-36.0;
        c1*(1.1400^c2)-38.6;
        c1*(1.49^c2)-43.7;
        ];

    A = [
        log(c1*0.9120)*(c1*0.9120)^c2 0.9120^c2;
        log(c1*0.9860)*(c1*0.9860)^c2 0.9860^c2;
        log(c1*1.0600)*(c1*1.0600)^c2 1.0600^c2;
        log(c1*1.1300)*(c1*1.1300)^c2 1.1300^c2;
        log(c1*1.19)*(c1*1.19)^c2 1.19^c2;
        log(c1*1.26)*(c1*1.26)^c2 1.26^c2;
        log(c1*1.3200)*(c1*1.3200)^c2 1.3200^c2;
        log(c1*1.3800)*(c1*1.3800)^c2 1.3800^c2;
        log(c1*1.1400)*(c1*1.1400)^c2 1.1400^c2;
        log(c1*1.49)*(c1*1.49)^c2 1.49^c2;
        ];
    
    AtA = A'*A;
    nAtR = -A'*r;
    v = AtA\nAtR;
    x0 = x0 + v
end